    public void preencherTabela() {
        DAOUsuarios u = new DAOUsuarios();
        List<Usuarios> lista = u.listarUsuarios();
        
        
        DefaultTableModel tableModel = (DefaultTableModel) tabelaUsuarios.getModel();
        tableModel.getDataVector().removeAllElements();
        
        for (Usuarios usuarios : lista) {
            ArrayList<String> linha = new ArrayList<String>();
            linha.add(usuarios.getNomeUsuario());
            linha.add(usuarios.getTelefoneUsuario());
            linha.add(usuarios.getCpfUsuario());
            
            tableModel.addRow(linha.toArray());
        }
    }   
  
        
    

